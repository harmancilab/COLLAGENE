#ifndef __STL_VECTOR_MACROS__
#define __STL_VECTOR_MACROS__

// Return integer size for a vector.
#define GET_VEC_SIZE(x) ((int)(x->size()))

// Shorthand, return integer size for a vector.
#define gvs(x) ((int)(x->size())) 

// Second shorthand, return integer size for a vector.
#define vecsize(x) ((int)(x->size())) 

#endif // __STL_VECTOR_MACROS__